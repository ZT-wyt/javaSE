package com.ZTday1;
/**
 * 使用变量名描述医院的信息
 * @author ZT
 * @date 2020年4月10日
 *
 */
public class HomeWork2 {
	
	public static void main(String[] args) {
		String hosName = "金坛市第一人民医院";//医院名称
		String address = "金坛市";//地址
		int sickRoomNum = 200; //病房数量
		int docNum = 100;      //医生人数
		String startTime = "9:00";   //上班时间
		String finishTime = "18:00"; //下班时间
		
		System.out.println("医院名称:" + hosName);
		System.out.println("地址:" + address);
		System.out.println("病房数量:" + sickRoomNum);
		System.out.println("医生人数:" + docNum);
		System.out.println("上班时间:" + startTime);
		System.out.println("下班时间:" + finishTime);
	}	
	
}
