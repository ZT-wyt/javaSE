package com.ZTday1;
/**
 * 使用变量名描述机票的信息
 * @author ZT
 * @date 2020年4月10日
 *
 */
public class HomeWork4 {
	public static void main(String[] args) {
		String flight = "FM9545"; //航班
		String seat = "7A";      //座位
		String shippingSpace = "W";       //舱位
		String date = "18MAY";     //日期
		String gate = "B03";       //登机口
		String name = "ZHOUTAO";    //姓名
		int boardingTime = 1140;    //登机时间
		String from = "BEIJING";    //始发站
		String to = "HONGKONG";     //到达站
		
		System.out.println("航班:" + flight);
		System.out.println("座位:" + seat);
		System.out.println("舱位:" + shippingSpace);
		System.out.println("日期:" + date);
		System.out.println("登机口:" + gate);
		System.out.println("姓名:" + name);
		System.out.println("登机时间:" + boardingTime);
		System.out.println("始发站:" + from);
		System.out.println("到达站:" + to);
		
		
	}

}
